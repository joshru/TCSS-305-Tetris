package actions;

import java.awt.event.ActionEvent;

import javax.swing.AbstractAction;
import javax.swing.JOptionPane;

/**
 * The Class ControlAction.
 * 
 * @author Josh Rueschenberg
 * @version 1.0
 */
@SuppressWarnings("serial")
public class ControlAction extends AbstractAction {

    /**
     * Instantiates a new control action.
     */
    public ControlAction() {
        super("Controls...");
    }
    
    /** 
     * @param theEvent the action event
     */
    @Override
    public void actionPerformed(final ActionEvent theEvent) {
        JOptionPane.showMessageDialog(null, "Board Controls:\n"
                        + "Move Left: A \nMove Right: D \nDrop Piece 1 Block: S \n"
                        + "Rotate Clockwise: E \nRotate Counter Clockwise: Q \n"
                        + "Hard Drop Piece: Space");

    }

}
